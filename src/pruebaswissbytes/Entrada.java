    private void btnMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoverActionPerformed
        // TODO add your handling code here:
       String mesetaCoordenadas=textTama単oMeseta.getText();
       //EXPRESION REGULAR PARA VALIDAR LA SECUENCIA DIGITO,ESPACIO,DIGITO
        if(!mesetaCoordenadas.matches("\\d+\\ \\d+")){
            JOptionPane.showMessageDialog(rootPane, "superficie incorrecta");
            return;
        }
        String posicion=textPosicionVehiculo.getText();
        //EXPRESION REGULAR PARA VALIDAR LA SECUENCIA DIGITO,ESPACIO,DIGITO,ESPACIO,DIRECCION
       if(!posicion.matches("\\d+\\ \\d+\\ \\w") ){
            JOptionPane.showMessageDialog(rootPane, "posicion incorrecta");
            return;
        }
         String ruta=textRuta.getText();
         //EXPRESION REGULAR PARA VALIDAR LOS POSIBLES MOVIMIENTOS L,R,M
        if(!ruta.matches("^[LRM]{1,100}$")||ruta.isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "ruta incorrecta");
            return;
        }
        
      
       String salida=calcular.calcular(mesetaCoordenadas, posicion,ruta);
       if(salida.equals("-1")){
           JOptionPane.showMessageDialog(rootPane,"la posicion es incorrecta");
            return;
       }
       
      JOptionPane.showMessageDialog(rootPane, salida);
        
    }//GEN-LAST:event_btnMoverActionPerformed

